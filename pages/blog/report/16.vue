            <p>スピーカーからつながっている線をたどって行けば何処のコネクタかわかります。</p>

            <h3>音声出力線を切って音を取る</h3>
            <p>一番簡単ですが、意外とハードルが高い方法です。<br>
            線を切断する必要があるので転売などを考えると不安が残ります。</p>
            <ul>
            <li>線を切らないといけない</li>
            </ul>
            <p>線を切ってそのままハイローコンバータへ接続します。</p>
            <h3>ハイローコンバータの入手と接続</h3>
            <p>ハイローコンバータはAmazonで購入できました。</p>
            <p>2chと4chの違いは入力音声のチャンネル数が異なります。<br>
            基本的にはパチスロの左右の音をとれば足りるのですが、一部の機種では上下でも違う音が出る機種もあります。<br>
            その場合、一部の演出音が取れなかったりする場合があるので、余裕があるのであれば4chがオススメです。<br>
            他にも4chで取ると音に深みが増すなどの感想も聞いたことがあります。</p>
            <p>値段の違いも気になるかと思います。安いものはやはり安いクオリティです。<br>
            しかし、普通に使っている分には安いもので困りません。<br>
            放送利用などで更なる高みを目指す方は高いものを利用するのも良いかと思います。</p>
            <p>接続方法ですが、購入したハイローコンバータに左右と、プラスマイナスの表示があるかと思うので、<br>
            それに従ってください。プラスマイナスは、左右を必ず揃えてください。<br>
            逆相接続（片方のプラスマイナスが間違っている）だと、音の違和感の原因となります。</p>
            <p>接続後は、アナログ音声ケーブルを延長し、ミキサーで音量調節し利用しています。</p>
