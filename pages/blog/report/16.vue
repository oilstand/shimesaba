            <ul>
            <li>ドアの開閉時に取れてしまう可能性</li>
            <li>筐体によっては接触が悪い場合がある</li>
            <li>クリップを繋ぐ場所が無い筐体もある</li>
            <li>スピーカーから音を出さないと音が取れない</li>
            </ul>
            <p>クリップが付けられればどの機種でも使えるというメリットもあります。</p>

            <h3>コネクタで音をとる</h3>
            <p>次に専用のコネクタを用意する方法は、クリップのデメリットはほぼ解消できます。<br>
            しかし、他のデメリットが発生します。</p>
            <ul>
            <li>コネクタを入手するのが難しい</li>
            </ul>
            <p>コネクタを入手するのは難しいです。<br>
